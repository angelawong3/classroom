// Ex:
// Input: 1
// Output: 'Monday'

// Input: 5
// Output: 'Friday'

// Input: 8
// Output: undefined

function nameOfWeekDay(day) {
  if (day === 1) {
    return "Monday";
  }
  if (day === 2) {
    return "Tuesday";
  }
  if (day === 3) {
    return "Wednesday";
  }
  if (day === 4) {
    return "Thursday";
  }
  if (day === 5) {
    return "Friday";
  }
  if (day === 6) {
    return "Saturday";
  }
  if (day === 7) {
    return "Sunday";
  }
}
